     */
    public function store(Request $request)
    {

       $request->validate([
           'new_file'=>'required ',
       ]);

        $path = Storage::putFile('images',$request->file('new_file'));

        return back()->withErrors(['sucess'=>' File  stored.'])->with('path',$path);
    }

